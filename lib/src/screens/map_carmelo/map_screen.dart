import 'package:flutter/material.dart';
import 'package:google_maps_flutter/google_maps_flutter.dart';

class GoogleMapScreen extends StatefulWidget {
  @override
  _GoogleMapScreenState createState() => _GoogleMapScreenState();
}

class _GoogleMapScreenState extends State<GoogleMapScreen> {
  final _initialCameraPosition=CameraPosition(target: LatLng(13.714168861279939, -89.15010894113253),zoom: 15);
  Set<Marker> _markers ={};


  void _onMapCreated(GoogleMapController controller){
    controller.setMapStyle(Utils.mapStyle);
    setState(() {
      _markers.add(
        Marker(
          markerId: MarkerId('id-1'), 
          position: LatLng(13.714196499330866, -89.15017721902265),
          infoWindow: InfoWindow(
            title: 'Clínica El Carmelo',
            snippet: 'PROVINCIA “NUESTRA SRA DE GUADALUPE”. Dirección: Avda. Francisco Palau, Prados de Venecia 2ª Etapa SOYAPANGO, San Salvador EL SALVADOR'
          )
        ),
      );

    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: PreferredSize(
          preferredSize: Size.fromHeight(80),
          child: AppBar(
          automaticallyImplyLeading: false,
          centerTitle: true,
          flexibleSpace: Container(
            decoration: BoxDecoration(
              image: DecorationImage( 
                image: AssetImage("assets/images/logo.png"), 
              )
            ),
          ),
          //title: Text('Clinica Carmelo'),
          actions: <Widget>[
            IconButton(onPressed: (){
             
            }, icon: Icon(Icons.logout), iconSize: 50,color: Color.fromRGBO(218, 186, 118, 1) ,)
          ],
          leading: IconButton(onPressed: (){
            Navigator.of(context).pop();
          }, icon: Icon(Icons.undo), iconSize: 60 ,color: Color.fromRGBO(218, 186, 118, 1) ,) ,
          backgroundColor: Colors.grey.shade800,
          ),
           
        ),
      body: GoogleMap(onMapCreated: _onMapCreated,
        markers: _markers,
        initialCameraPosition: _initialCameraPosition,),
    );
    
  }
}

